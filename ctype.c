#include "ctype.h"

//-------------------------------------------------------------------------------
// lowercase letter
//-------------------------------------------------------------------------------
int islower(int c)
{
	return (c >= 'a' && c <= 'z');
}

//-------------------------------------------------------------------------------
// uppercase letter
//-------------------------------------------------------------------------------
int isupper(int c)
{
	return (c >= 'A' && c <= 'Z');
}

//-------------------------------------------------------------------------------
// alphabetic character
//-------------------------------------------------------------------------------
int isalpha(int c)
{
	return islower(c) || isupper(c);
}

//-------------------------------------------------------------------------------
// decimal digit character
//-------------------------------------------------------------------------------
int isdigit(int c)
{
	return (c >= '0' && c <= '9');
}

//-------------------------------------------------------------------------------
// alphanumeric character
//-------------------------------------------------------------------------------
int isalnum(int c)
{
	return isalpha(c) || isdigit(c);
}

//-------------------------------------------------------------------------------
// convert to lowercase
//-------------------------------------------------------------------------------
int tolower(int c)
{
	if (!isupper(c))
		return c;

	return c + 32;
}

//-------------------------------------------------------------------------------
// convert to uppercase
//-------------------------------------------------------------------------------
int toupper(int c)
{
	if (!islower(c))
		return c;

	return c - 32;
}

//-------------------------------------------------------------------------------
// whitespace character
//-------------------------------------------------------------------------------
int isspace(int c)
{
	return c == ' '
		|| c == '\n'
		|| c == '\r'
		|| c == '\t'
		|| c == '\f';
}

//-------------------------------------------------------------------------------
// printing character, including space
//-------------------------------------------------------------------------------
int isprint(int c)
{
	return (c >= 32 && c <= 126);
}

//-------------------------------------------------------------------------------
// graphical character, excluding space
//-------------------------------------------------------------------------------
int isgraph(int c)
{
	return (c >= 33 && c <= 126);
}

//-------------------------------------------------------------------------------
// punctuation character
//-------------------------------------------------------------------------------
int ispunct(int c)
{
	return isgraph(c) && !isalnum(c);
}

//-------------------------------------------------------------------------------
// hexadecimal digit character
//-------------------------------------------------------------------------------
int isxdigit(int c)
{
	return isdigit(c) || (c >= 'a' && c <= 'f') || (c >= 'A' && c <= 'F');
}

//-------------------------------------------------------------------------------
// control character
//-------------------------------------------------------------------------------
int iscntrl(int c)
{
	return (c >= 0 && c <= 31) || c == 127;
}

//-------------------------------------------------------------------------------
// blank character (space or tab)
//-------------------------------------------------------------------------------
int isblank(int c)
{
	return c == ' ' || c == '\t';
}
